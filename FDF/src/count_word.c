#include "../include/fdf.h"

int	count_word(const char *s, char c)
{
	int	i;
	int	wc;

	i = 1;
	wc = 0;
	if (s[0] != c)
		wc++;
	while (s[i])
	{
		if ((s[i - 1] == c && s[i] != c))
			wc++;
		i++;
	}
	return (wc);
}
